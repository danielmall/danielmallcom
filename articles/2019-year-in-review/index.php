                <h2 id="videos-photos">Videos &amp; photos <a class="dm-dp-anchorLink" href="#videos-photos">#</a></h2>

                <p>In last year&rsquo;s review, I mentioned that I had &ldquo;caught the video bug&rdquo; and that I&rsquo;d be posting a lot more videos this year.</p>

                <p>Nope.</p>

                <p>I did <em>shoot</em> a lot more video this year, but I didn&rsquo;t really post many because, well, they suck. I&rsquo;ve been shooting a lot more videos with my <abbr title="Digital Single Lens Reflex">DSLR</abbr>, mirrorless, and GoPro, but I don&rsquo;t like any of them. And geez, editing takes <em>so long</em>. I&rsquo;m not sure that I&rsquo;m willing to put the time in to get good at this. I&rsquo;m not completely writing it off, but I&rsquo;m also not exactly enthusiastic about it. I do have a few ideas for online courses I&rsquo;d like to make, so perhaps that&rsquo;ll be the motivation that gets me over the hump.</p>

                <p>On the other hand, I did have a lot of renewed interest in photography this year. I think the change started last year when I attended the <a href="https://f8workshops.com/faroe-islands-april-2018">f/8 Photography Workshop in the Faroe Islands</a>. The weather conditions and subjects made it ideal for taking great photos, and I learned so much from the great instructors about both editing and shooting.</p>

                <p>This year, I took two online courses that I found through Instagram (I&rsquo;m such a sucker for Instagram ads):</p>

                <ol>
                    <li><a href="https://expertphotography.com/store/courses/p11-photography-for-beginners/">Photography for Beginners</a>, by Josh Dunlop at Expert Photography. I learned to shoot in a high school photography class on a manual camera that was either my mom&rsquo;s or came from a flea market, which gave me a decent enough understanding of the mechanics of shooting. But I never really practiced enough to get really good at it. Even though a lot of this course was review for me, it covered all of the gaps of things I missed in the transition from using a manual camera to a digital one over the last 20 years.</li>
                    <li><a href="https://www.learn.photographyacademy.com/webclass">Photography Academy</a>, by Tim Shields. I really love Tim&rsquo;s simple approach to teaching photography. He explains things slowly and thoroughly without coming off patronizing or like he&rsquo;s dumbing it down. He suggests that there are only two things important about photography: getting sharp images and getting proper exposure. His courses are about many different but still simple ways to achieve those two goals.</li>
                </ol>

                <p>In my shooting even while taking these courses, my images improved dramatically. As a designer, I&rsquo;m pretty experienced at taking even the worst images and making them look good. I realized that the photos I was taking weren&rsquo;t good; I was just good and making them look good afterwards. But with the tips I picked up over the last two years, the images coming out of my camera <em>start</em> at a much higher level, so I&rsquo;m working with significantly better source imagery, which is candy for a designer.</p>

                <p>Unlike my video work, I&rsquo;m able to create photos that I really like, which incentives me to shoot and post more. I created <a href="http://photos.danmall.me/">a little site where I&rsquo;ll be posting my photos</a>, and you can also follow me on my public Instagram account <a href="https://www.instagram.com/danmallphotos/">@danmallphotos</a> dedicated to my photography journey. I&rsquo;m excited about doing a <a href="https://jeffbartlettmedia.com/workshops/canadian-rockies/">one-day workshop in the Canadian Rockies with Jeff Barlett</a> in a few weeks and am determined to take a few more trips like this throughout the year to practice photography even more.</p>

