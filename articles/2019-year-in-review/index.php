                <h2 id="decade-in-review">A decade in review <a class="dm-dp-anchorLink" href="#decade-in-review">#</a></h2>

                <p>As we ring in the new year and new decade, it seems like a good time to for me to look back at the last year and decade and take inventory.</p>

                <p>In 2009, <a href="http://emilymall.me/">Em</a > and I left Philly where she had lived for over a decade and I had lived for most almost two decades to try on New York as one of our first big adventures as a married couple.</p>

                <p>I <a href="http://v2.danielmall.com/archives/2009/07/16/cogs_and_spaceships.php">left a dream job</a> at <a href="https://www.happycog.com/">Happy Cog</a>, turned down a dream offer from Facebook, a took another dream job at <a href="https://www.bigspaceship.com/">Big Spaceship</a>. (And yes, I know how privileged I am to have any one of those things, much less all three. I don&rsquo;t take any of it for granted. I have nothing but extreme gratitude in my heart for all of those opportunities and do my best to pass it on and provide the same opportunities for as many others as possible.)</p>

                <p>On this day in 2009, I awoke to 7 missed calls from my Mom, saying my Grandma passed away peacefully in her sleep. I miss her terribly every day, but the hundreds of memories I have of her make me smile every time I think of her.</p>

                <p>In the decade since, we&rsquo;ve moved back to Philly with renewed fervor for the city, <a href="http://v3.danielmall.com/articles/leaving-four24/">quit my band</a>, created two spectacular daughters, <a href="http://v3.danielmall.com/articles/introducing-superfriendly/">started an agency</a>, started (and left) <a href="http://www.businessology.biz/">a podcast</a>, <a href="https://abookapart.com/products/pricing-design">wrote a book</a>, started (and left) <a href="https://superbooked.com/">a <abbr title="Software as a Service">SaaS</abbr> product</a>, ate some delicious food, and traveled around the world.</p>
                
                <p>I live an incredibly blessed life, and I guard it relentlessly.</p>

                <p>Here&rsquo;s what&rsquo;s happened in the last year.</p>

                <h2 id="eleven">Eleven <a class="dm-dp-anchorLink" href="#eleven">#</a></h2>

                <p role="text">Em and I celebrated our 11th anniversary in 2019! We were going to throw a big bash called the <span aria-hidden="true">Ma11 Ba11</span><span class="dm-u-move--offscreen"> Mall Ball</span>&mdash;get it?&mdash;but then we realized that it would really stress out these two introverts to plan and throw a giant party and that we&rsquo;d much rather travel together. So we spent a long weekend in Cuba instead!</p>

                <h2 id="kids-family">Kids &amp; family <a class="dm-dp-anchorLink" href="#kids-family">#</a></h2>

                <p>Overall, our family had lots of fun adventures together in 2019. Sidda and Charlie are now 8 years old and 6 years old respectively. There&rsquo;s lots of interesting things to share about them, but I&rsquo;m also increasingly more inclined to keep their personal details private. Suffice it to say that the older they get, the more I&rsquo;m enjoying being their dad. And the more the four of us grow together,the deeper and more interesting and exciting our family relationship gets. It&rsquo;s easy at times and difficult at times, and I&rsquo;m lucky to be a part of all of it.</p>

                <h2 id="max">Max <a class="dm-dp-anchorLink" href="#max">#</a></h2>

                <img src="max.jpg" alt="Max" />

                <p>We got a dog! This is our second try with dogs: we had to give our Rottweiler puppy to a friend after not even a week as we discovered our family was allergic. We also had cats this time last year, but same deal: allergies meant we had to give them away. We tried fish too, but they died.</p>

                <p>But Max is a Yorkshire Terrier, which apparently is a hypoallergenic breed since they have hair (not fur) and don&rsquo;t shed. We&rsquo;ve had him for a little over a month now without any major issue, so it seems like he&rsquo;s a keeper.</p>

                <p>As someone who didn&rsquo;t have pets growing up and is kinda afraid of dogs, it&rsquo;s definitely an adjustment. Since I shut down my apprenticeship last year, my office is usually pretty quiet and lonely now, but Max is a great office buddy. He gives me lots of reasons to step away from work every so often to play. I do wish I could entertain him more than I do. He mostly sleeps all day, but for a puppy, I&rsquo;d imagine he&rsquo;d like to be more active.</p>


                <h2 id="health-wellness-self-care">Health, wellness, and self-care <a class="dm-dp-anchorLink" href="#health-wellness-self-care">#</a></h2>

                <p>I&rsquo;ve been drinking a lot more water this year, thanks to always having my <a href="https://www.hydroflask.com/21-oz-standard-mouth-flex/color,lava,a,92,o,54">HydroFlask</a> or <a href="https://amzn.to/2tjcV8p">Coca-Cola tumblers</a> filled with ice water nearby. I thought this would help me drink less soda, but alas, I&rsquo;m still as addicted to cola as ever. (IT&rsquo;S SO DELICIOUS.)</p>

                <p>I&rsquo;m happy to report that I&rsquo;m still playing basketball regularly, an average of once every two weeks. An ankle sprain about 2 months ago has put me out of commission recently, but I&rsquo;m <a href="https://twitter.com/danmall/status/1211726228979355648">slowly getting back into athletic activity again</a>.</p>

                <p>One thing I&rsquo;m really happy about is that I&rsquo;m eating at home more now. I really enjoy cooking, but very rarely do I block out the time to do it. So, I end up defaulting to fast food a lot. Over the last 6 months, however, subscriptions to meal services <a href="https://my.tovala.com/referral/JPP5WDZR">Tovala</a> and <a href="https://eat.cookunity.com/landing-referral?referral_code=danmal96/">CookUnity</a> have got me eating quick and delicious meals, and I&rsquo;m also saving a little money every month too.</p>

                <p><a href="/articles/my-experience-with-laser-eye-surgery/">I finally got laser eye surgery</a> this year. <abbr title="Too long; didn&rsquo;t read">TL;DR</abbr>: if you can deal with a first few hours of inconvenience, it&rsquo;s awesome. Almost one year later, I&rsquo;m really loving the little things, like forgetting to bring contact solution on a trip or worrying about a contact lens getting bumped out while playing sports.</p>

                <p>I wanted to get my second tattoo this year, but it didn&rsquo;t happen. But! It&rsquo;s definitely happening in 2020. I have the appointment already booked, and bonus: my brother and I are gonna get the same one together.</p>

                <p>I started therapy again this year after about a year-long break. It&rsquo;s been helpful at times, but I think I need to be clearer with myself (and my therapist) about what I want out of it.</p>

