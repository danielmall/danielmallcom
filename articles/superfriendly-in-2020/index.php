                <p><span class="dm-dp-openingLines">I believe</span> that 2020 will be a big year for <a href="https://superfriendlydesign.systems/">SuperFriendly</a>.</p>
                
                <p>Midway through 2019, we officially and publicly repositioned the company to focus solely on <a href="https://superfriendlydesign.systems/glossary/#design-system">design systems</a>. An overwhelming majority of the work we were doing over the last few years was design systems work anyway, so we decided to be upfront about this focus in hopes that <a href="http://v3.danielmall.com/articles/oil-change-pizza/">the narrower positioning</a> would bring some advantages.</p>

