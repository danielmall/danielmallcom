                <h2 id="global-pandemic">A global pandemic <a class="dm-dp-anchorLink" href="#global-pandemic">#</a></h2>

                <p>I certainly can&rsquo;t talk about business in 2020 without talking about <abbr title="2019 novel coronavirus">COVID</abbr>-19. This global pandemic had a major impact on SuperFriendly this past year, and I&rsquo;m surprised to say almost all effects&mdash;at least the ones that are typically tracked in a spreadsheet&hellip; more on this later&mdash;have been positive. Financially, 2020 was SuperFriendly&rsquo;s best year by far; some of that was because of plans we&rsquo;ve made that manifest this year and some of that was actually <em>because</em> of <abbr>COVID</abbr>. That makes me feel relieved, proud, hopeful, sad, and ashamed, all at the same time. I&rsquo;m conflicted about feeling successful in a year that&rsquo;s been so devastating for so many people on the planet. (I&rsquo;m actively working on this with my therapists and coaches.) What keeps me distracted from being consumed by that emotional conflict is focusing on using SuperFriendly to continue to create better opportunities for our SuperFriends and clients.</p>

                <p>At the beginning of the year, we were working with lots of clients in the travel and hospitality industry, like <a href="https://www.united.com/">United Airlines</a> and <a href="https://www.marriott.com/">Marriott</a>. When the pandemic hit in March, those companies had more existential issues to focus on than their work with us (rightly so), so most of that work dried up quickly.</p>

                <p>While our travel and hospitality work was dwindling, it&rsquo;s really only by good fortune that our work with healthcare, entertainment, and education clients was ramping up, some actually <em>because</em> of the needs that a global pandemic was causing. For example, our work with clients like <a href="https://www.hermanmiller.com/">Herman Miller</a> and <a href="https://www.hbs.edu/Pages/default.aspx">Harvard Business School</a> turned toward topics like helping people make a transition to remote work or forming opinions about what events look like in a socially-distanced world. We created &ldquo;response teams&rdquo; to brainstorm with our clients about things that might be helpful to them and their customers, from creating new products and offerings to discounting our own services or offering pro bono work to help them through a tumultuous time.</p>

                <p>When people asked me earlier this year how SuperFriendly was doing, my typical response was to include some mention of volatility. <abbr>COVID</abbr> made our business more volatile: more clients were stopping business with us all at once than ever before, but our pipeline was more full than ever too, so it was mostly more activity than we were used to.</p>

                <p>Now, at the end of the year, I can say that the volatility has worn off a bit, but our pipeline remains more full than it&rsquo;s ever been and we have more prospects, projects, and accounts lined up for the new year than we&rsquo;ve ever had at the end of a calendar year.</p>


                <h2 id="systems-team">The SuperFriendly Systems team <a class="dm-dp-anchorLink" href="#systems-team">#</a></h2>

                <p>Those familiar with SuperFriendly for a while probably know the SuperFriend model: I&rsquo;m the only employee (as I&rsquo;ve been since <a href="http://v3.danielmall.com/articles/introducing-superfriendly/">I started it in 2012</a>), and every project we do is a custom-assembled team for that client. That said, I&rsquo;m not a fan of using the royal &ldquo;we&rdquo; like many do when talking about their offerings; I try hard to say &ldquo;I&rdquo; when I&rsquo;m talking about what I&rsquo;m doing personally and use &ldquo;we&rdquo; to refer to a larger group of SuperFriends.</p>

                <p>When I talk about what we at SuperFriendly did in 2020, I do mean it&rsquo;s the effort of a group of extraordinary people that&rsquo;s more than just me. In the world of enterprises that need and use <a href="https://superfriendlydesign.systems/glossary/#design-system">design systems</a>, a common model is to have dedicated teams working on separate products or features and then having a more central &ldquo;design system team&rdquo; that serves each of the product/feature teams. For SuperFriendly, instead of product teams, we have project/account teams that serve each client, so we established a &ldquo;Systems&rdquo; team in 2020 to serve each of those project/account teams.</p>

                <p>On our Systems team, we have roles for representatives from these disciplines: executive, business development, operations, administrative, and legal. They support our project teams in all of these areas. The people that represent each discipline work primarily on SuperFriendly, as opposed to other SuperFriends who work primarily on client work. Without these people, SuperFriendly wouldn&rsquo;t be nearly what it was this year:</p>

                <ul>
                    <li><a href="https://www.sherlocklegal.com/">Matt Sherlock</a> is SuperFriendly&rsquo;s attorney (and also a friend I&rsquo;ve known since high school). Matt does more than just weigh in on agreements; he regularly advises SuperFriendly and helps shape its direction from a risk profile standpoint and has since 2013.</li>
                    <li><a href="https://www.linkedin.com/in/nikki-loffredo-5a8b1945/">Nikki Loffredo</a> handles all of SuperFriendly&rsquo;s administrative needs. She makes it all run smoothly, from bookkeeping, invoicing, reimbursements, travel arrangements, and lots more. She&rsquo;s been a concierge for SuperFriends since 2013.</li>
                    <li><a href="https://twitter.com/crystalvitelli">Crystal Vitelli</a> runs all of SuperFriendly&rsquo;s operations. She acts as a coach and support system for our Producers and Executive Producers. Crystal used to be a SuperFriendly client and has produced SuperFriendly projects since 2016, so she understands both sides of a client relationship better than any other SuperFriend.</li>                    
                    <li><a href="https://twitter.com/joerinaldi">Joe Rinaldi</a> runs all of SuperFriendly&rsquo;s new business. Just about every dollar that SuperFriendly made in 2020 was at least partially shaped by Joe, if not fully.</li>                    
                    <li>I help set direction for the company and provide some shape for some of SuperFriendly&rsquo;s unique services. Most of my time goes to some split of interviewing new SuperFriends, coaching, building teams, shaping and pricing new work, and creating structure.</li>
                </ul>

                <p>In addition to these folks, we hold 2 spots open for SuperFriends that primarily work on projects to weigh in on how SuperFriendly can better support teams from that perspective. For the last 6 months, <a href="https://twitter.com/iamlaurendeal/">Lauren Deal</a> and <a href="https://dontaebenn.myportfolio.com/">Dontae Benn</a> have been invaluable in providing their insight on what a good balance between support and autonomy can look like for teams.</p>


                <h2 id="superfriendly-in-numbers">SuperFriendly in numbers <a class="dm-dp-anchorLink" href="#superfriendly-in-numbers">#</a></h2>

                <p>Over to some quantitative fun! Here are some numbers that illustrate parts of what SuperFriendly looked like all year.</p>

                <h3 id="time-spent">Time spent <a class="dm-dp-anchorLink" href="#time-spent">#</a></h3>

                <p>I&rsquo;ve generally been a proponent of the &ldquo;work smarter, not harder&rdquo; adage, so I&rsquo;m often looking for ways to spend less time working without much significant impact on my productivity.</p>
                
                <ul>
                    <li>In 2019, I worked <strong>2,040 hours</strong>, an average of <strong>39 hours/week</strong>.</li>
                    <li>In 2020, I worked <strong>1,903 hours</strong>, an average of <strong>37 hours/week</strong>.</li>
                </ul>

                <p>Here&rsquo;s generally how my detailed time spent breaks down.</p>

                <ul>
                    <li><strong>Admin:</strong> 372 hours (20%)</li>
                    <li><strong>Creative Direction:</strong> 333 hours (17%)</li>
                    <li><strong>Business Development:</strong> 277 hours (15%)</li>
                    <li><strong>Vacation:</strong> 264 hours (14%)</li>
                    <li><strong>Speaking/Conferences:</strong> 178 hours (9%)</li>
                    <li><strong>Coaching:</strong> 138 hours (7%)</li>
                    <li><strong>Marketing:</strong> 110 hours (6%)</li>
                    <li><strong>Design:</strong> 56 hours (3%)</li>
                    <li><strong>Writing:</strong> 39 hours (2%)</li>
                    <li><strong>Miscellaneous/Other:</strong> 136 hours (7%)</li>
                </ul>

                <p>This is the first year since I opened SuperFriendly in 2012 that I spent more time on Administrative things (documentation, reviewing, planning, etc.) than either Creative Direction or Business Development. This affirms and confirms my decision to want to spend more of my effort on SuperFriendly itself than on client work.</p>


                <h3 id="projects">Projects <a class="dm-dp-anchorLink" href="#projects">#</a></h3>

                <p>We did significantly more projects this year than any other. We average about 9 projects/year. In 2020, we did <strong>19 projects</strong>, a 111% increase from 2019&rsquo;s 9 projects.</p>

                <p>We worked on some cool things, like:</p>

                <ul>
                    <li>A <a href="https://www.enotes.com/">new site for eNotes</a></li>
                    <li>An enterprise-wide design system for <a href="https://www.united.com/en/us">United Airlines</a></li>                    
                    <li>A new design language and templates for the University of Pittsburgh that power both their <a href="https://admissions.pitt.edu/">Admissions</a> and <a href="https://financialaid.pitt.edu/">Financial Aid</a> sites</li>
                    <li>Some ecosystem strategy work for <a href="https://www.marriott.com/">Marriott</a></li>
                    <li>Some product prototypes for <a href="https://www.hermanmiller.com/">Herman Miller</a></li>
                    <li>Design system work for <a href="https://www.pfizer.com/">Pfizer</a></li>
                    <li>An evolved visual identity for <a href="https://boardable.com/">Boardable</a> that&rsquo;s soon to roll out</li>
                    <li>An updated site for the <a href="https://digital.hbs.edu/">Digital Initiative</a> at Harvard Business School</li>
                    <li>A web application for the State of Maine</li>
                    <li>An annual report for the <a href="https://www.steveandalex.org/">Steven &amp; Alexandra Cohen Foundation</a></li>
                    <li>Work you&rsquo;ll see soon for <a href="https://www.familylife.com/">FamilyLife</a>, <a href="https://www.twilio.com/">Twilio</a>, the <a href="https://faithandliberty.org/">Faith &amp; Liberty Discovery Center</a>, <a href="https://www.point72.com/">Point72</a>, and more</li>
                </ul>               


                <h3 id="revenue-profit">Revenue &amp; Profit <a class="dm-dp-anchorLink" href="#revenue-profit">#</a></h3>

                <p>SuperFriendly made more gross revenue in 2020 than in any other year. 2019 was the best year to date, and we increased that by 145% in 2020.</p>

                <p>However, we only made 11% profit on our revenue in 2020, which is down 17% from the 28% profit margin we had in 2019. I haven&rsquo;t dug in closely enough to know exactly what the cause of that drop was, but my hunch is that our significant increase in overhead this really ate into what would have otherwise been profit. That&rsquo;s definitely something to correct for 2021.</p>


                


                <h3 id="people-purpose">People &amp; Purpose <a class="dm-dp-anchorLink" href="#people-purpose">#</a></h3>

                <p>I saved the most important one for last.</p>

                <p><a href="https://superfriendlydesign.systems/purpose/">SuperFriendly&rsquo;s purpose</a> is to <strong>create better opportunities for creative people to flourish</strong>. (I&rsquo;ll be revising this in 2021, but more on that another time.) If we have <a href="http://leananalyticsbook.com/one-metric-that-matters/">one metric that matters</a>, it&rsquo;s creating better opportunities, whether that&rsquo;s helping SuperFriends meet financial goals, learn something new, work with a specific client, gain mentorship from more experienced folks, and more.</p>

                <p>At the end of 2019, <a href="/articles/superfriendly-in-2020/#purpose-driven-goals">we made a commitment</a> to do a better job in hiring more women, people of color, and other groups of people typically underrepresented in the tech industry. And especially with the murders of George Floyd, Breona Taylor, and many other Black people in 2020 and over the previous few years, we renewed that commitment to creating better opportunities specfically for Black people at SuperFriendly.</p>

                <p>Here are some of the specific goals we set last year in who we wanted our SuperFriends to be and how we did against all of them.</p>

                <ol>   
                    <li>We wanted to hire at least 30 SuperFriends this year. We hired <strong>55 SuperFriends</strong>.</li>
                    <li>We wanted to pay all SuperFriends a total of $500,000, an average of just under $17,000 each. We paid SuperFriends <strong>$2,333,039.62</strong>, <strong>an average of $42,418.90 each</strong>.</li>
                    <li>We wanted to hire at least 15 women (50% of projected hires). We hired <strong>23 women</strong> (42% of our hires in 2020).</li>
                    <li>
                        We wanted to hire at least 15 people of color (50% of projected hires). We hired <strong>21 people of color</strong> (38% of our hires in 2020).
                        <ul>
                            <li>We hired <strong>10 Black people</strong>: 4 women (40%) and 6 men (60%).</li>
                            <li>We hired <strong>6 Hispanic people</strong>: 2 women (33%) and 4 men (67%).</li>
                            <li>We hired <strong>4 Asian people</strong>: 3 women (75%) and 1 man (25%).</li>
                            <li>We hired <strong>1 Middle Eastern woman</strong>.</li>
                        </ul>
                    </li>
                </ol>

                <p>I&rsquo;m incredibly proud of all of those things! But there&rsquo;s still a lot of work to be done and things to fix here. For example, we paid 12 SuperFriends above our average of $42,418.90. Of those 12: 10 are white, and 10 are men. So, while we&rsquo;ve done a better job of hiring more women and people of color, having them be on the lower end of our pay scale isn&rsquo;t enough. In 2021, I want to be able to say our highest paid SuperFriends are women and people of color. I don&rsquo;t know exactly how we&rsquo;re going to do that yet, but I&rsquo;ll be working on that immediately.</p>


                <h3 id="superfriendly-in-2021">SuperFriendly in 2021 <a class="dm-dp-anchorLink" href="#superfriendly-in-2021">#</a></h3>

                <p>One of the biggest changes I&rsquo;m going to work on is around culture. In 2020, SuperFriends spent a lot of time focusing on how to do good work/good projects. That&rsquo;s nice, but they didn&rsquo;t focus as much on how to work together better. We spent too much time on the &ldquo;project&rdquo; part and not enough time on the &ldquo;people&rdquo; part. I think some of that is the default culture of freelancing, which is inherently tied into our model. Unlike an agency where you get hired full-time, freelancers default into the &ldquo;hired gun&rdquo; mentality where you come in, do your job with your head down, and fly out. I want SuperFriendly to be about teams. I want it to be a place where freelancers can rely on others but can still exercise the value of being independent that attracted them to freelancing in the first place. We haven&rsquo;t done a good job of creating the environment for that, so I&rsquo;ll be actively working on that in 2021.</p>

                <p>That&rsquo;s not across the board though; 2 of our teams did a particularly good job with not just acting like a bunch of freelancers but actually being a team. One of our teams saw their project come to a halt because of <abbr>COVID</abbr>, but the bond they created lasted long after their project was over. Another one of our teams was very intentional about developing their own team culture. They created their own team shirts, regularly eat together over Zoom, and build each other up on a daily basis. It&rsquo;s no surprise that this team&rsquo;s work is some of the most exciting at SuperFriendly right now.</p>

                <p>In short, we didn&rsquo;t spend enough time on people in 2020. (And partially justified too; for some, &ldquo;doing a good job&rdquo; was all they had to offer in 2020, and that&rsquo;s absolutely enough.) Still though, 2021 will be about SuperFriends. We haven&rsquo;t done a great job of sharing much lately about our process, our work, our values, and our purpose. A big project for me in 2021 will be to share more about how we do things, recruit even more SuperFriends, and create a culture where taking risks is safe and fun.</p>

                <p>Overall, I&rsquo;m glad 2020 is over and I&rsquo;m also grateful to have gone through it in a way that could still create great opportunities for SuperFriends.</p>
                
                <p>Bring on 2021.</p>

