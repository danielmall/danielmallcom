                <p><span class="dm-dp-openingLines">At the end of 2019, <a href="/articles/superfriendly-in-2020/">I predicted and hoped</a></span> that 2020 would be a big year for <a href="https://superfriendlydesign.systems/">SuperFriendly</a>. I didn&rsquo;t know the half of it.</p>

