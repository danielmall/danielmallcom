                <h2 id="spoilers">Spoilers <a class="dm-dp-anchorLink" href="#spoilers">#</a></h2>

                <p>I&rsquo;m saying it again: spoilers included below. If you haven&rsquo;t seen <cite>Endgame</cite> yet and you&rsquo;re the kind of person that gets upset if they know things going into a movie, don&rsquo;t read any further. Spoilers included for not just <cite>Endgame</cite> but also <cite>Interstellar</cite>, <cite>Looper</cite>, <cite>Back to the Future</cite>, <cite>The Time Traveler&rsquo;s Wife</cite>, <cite>Ant-Man and the Wasp</cite>, <cite>Doctor Strange</cite>, <cite>Spider-Man: Into the Spider Verse</cite>, and <cite>Infinity War</cite>. You&rsquo;ve been warned.</p>

                <p style="margin-bottom: 16em;">As my buddies on <a href="https://twitter.com/comicbookjunto?lang=en">Comic Book Junto</a> say, 3&hellip; 2&hellip; 1&hellip; if you&rsquo;re reading past this point, it&rsquo;s your fault.</p>

                <p>Here goes.</p>


                <h2 id="summary">Summary <a class="dm-dp-anchorLink" href="#summary">#</a></h2>

                <p>Overall, the movie was great. As the final chapter in an 11-year, 22-movie arc, the stakes couldn&rsquo;t have been higher. And <cite>Endgame</cite> delivered.</p>

                <p>The amount of characters and storylines to incorporate was staggering. But what <a href="https://www.themarysue.com/whedon-ultron-nightmare/">Joss Whedon failed to do</a> in <cite>Age of Ultron</cite>, <cite>Endgame</cite> directors Anthony and Joe Russo and writers Stephen McFeely and Christopher Markus handled as well as could be imagined.</p>

                <p>I&rsquo;ve only seen it once so far and intend to many times over, but I doubt I&rsquo;ll much of my positive opinion will change upon subsequent viewings. There is one thing, though, that I take issue with: time travel.</p>


                <h2 id="time-travel">Time travel <a class="dm-dp-anchorLink" href="#time-travel">#</a></h2>

                <p>When we first sat down in the theater, <a href="https://twitter.com/emilyjanemall">Em</a> nonchalantly whispered to me, &ldquo;I bet they go back in time and everyone comes back to life.&rdquo; <?php /* ?><sup class="dm-c-sidenotes--link"><a href="#note1">1</a></sup><?php */ ?></p>

                <?php /* ?>
                <aside id="note1" class="dm-c-sidenotes dm-l-articleGrid--sidenote">
                    <p>I go to great lengths to avoid spoilers, which to me means anything that reveals <em>anything</em> about a movie. That&rsquo;s a trailer, a poster, a plot point, fan theories&hellip; anything. My most enjoyable movie viewings are ones where I have no clue what&rsquo;s happening. Any expectations I have really affect my movie experiences.</p>
                </aside>
                <?php */ ?>

                <p>I replied, &ldquo;Gosh, I hope not.&rdquo;</p>

                <p>And then Scott Lang goes on to tell everybody about the quantum realm and seals the deal with the phrase &ldquo;time heist,&rdquo; which makes me involuntary produce a simultaneous &ldquo;sigh &amp; eye-roll&rdquo; combo a suburban Philadelphia movie theater has ever seen.</p>

                <p>Why don&rsquo;t I like time travel? Two reasons: it&rsquo;s confusing and disrespectful.</p>

                <h2 id="time-travel-is-confusing">Time travel is confusing <a class="dm-dp-anchorLink" href="#time-travel-is-confusing">#</a></h2>

                <p>As soon as you open the &ldquo;time travel&rdquo; door, things get&hellip; science-y. Even if you don&rsquo;t know it specifically, time travel is about <a href="https://en.wikipedia.org/wiki/Special_relativity">special relativity</a>, which quickly brings up questions like the <a href="https://en.wikipedia.org/wiki/Twin_paradox">twin paradox</a>, <a href="https://en.wikipedia.org/wiki/Grandfather_paradox">grandfather paradox</a>, and the like.</p>

                <p>So, it stands to reason that plots based around time travel bring up these questions. How do you solve this? In my opinion, the movies that solve this best are the ones <em>about</em> time travel directly. Time travel isn&rsquo;t just a plot device; it is <em>the</em> plot device.</p>

                <ul>
                    <li><cite>Back to the Future</cite> is a great example. To reduce confusion, it stuck with a single timeline where the events of the past can affect the future. I believe the simplicity of the story is why it remain our collective logical explanation of how time travel works, so much so that <cite>Endgame</cite> had to spend time trying to convince us <em>not</em> to consider this model.</li>
                    <li><cite>The Time Traveler&rsquo;s Wife</cite> is all about the confusion that time travel creates.</li>
                    <li><cite>Looper</cite> tackles the grandfather paradox head on.</li>
                </ul>

                <p>What about movies that aren&rsquo;t <em>about</em> time travel that still involve it? <cite>Interstellar</cite> capitalizes on the fact that the simplest explanation for the twin paradox is wormholes, and since they&rsquo;re in space, this coincidentally fits. Like <cite>Back to the Future</cite>, the simplicity makes it a convincing enough explanation.</p>

                <p>The <abbr title="Marvel Cinematic Universe">MCU</abbr> has even utilized this same technique. Janet van Dyne really shouldn&rsquo;t have aged in the quantum realm, but it would have been weirder for Hank and Hope (and the audience) if she didn&rsquo;t, so we went with it. In <cite>Infinity War</cite>, Thanos uses the Time Stone to reverse a tiny bit of time to get the Mind Stone from Vision&rsquo;s head after Scarlet Witch destroys it&mdash;which is crazy confusing!&mdash;but we go with it because our suspension of disbelief just says that that&rsquo;s how the Time Stone works.</p>

                <p>So, instead of making all of Act II a confusing time heist, there was another option available that&rsquo;s a bit more <a href="https://en.wikipedia.org/wiki/Occam%27s_razor">Occam&rsquo;s razor</a>-like: the multiverse. <cite>Doctor Strange</cite> introduced it, <cite>Spider-Man: Into the Spider-Verse</cite> (despite not officially being part of the <abbr>MCU</abbr>) gave us a great story with it at the core &hellip; heck, the Ancient One even reminded us of it. In my opinion, this could have also gotten us to an interesting place, perhaps even moreso than where the current plot left us. More on this later.</p>

                <p>Even more significant than realizing that time travel is confusing, time travel is disrespectful to the audience.</p>


                <h2 id="time-travel-is-disrespectful">Time travel is disrespectful <a class="dm-dp-anchorLink" href="#time-travel-is-disrespectful">#</a></h2>

                <p>Few things make comic book fans groan as much as <a href="https://en.wikipedia.org/wiki/Retroactive_continuity">retcons</a>. The late 90s and early aughts were where many of us stopped reading comics, and I think it&rsquo;s no coincidence that that time was also rampant with retcons&mdash;from Spider-Man clones to the real story of Wolverine claws to teenage Iron Man and much more.</p>

                <p>Time travel is a close second to retcons.</p>

                <p>One of the reasons <cite>Infinity War</cite> was so great was because of the the impact of The Decimation, more commonly known as &ldquo;the snap.&rdquo; Thanos erases half the population of the entire universe, and that&rsquo;s how the movie ends?! What drama! It left viewers reeling, myself included, just plain shocked and in disbelief that any movie&mdash;much less part 1 of the end of this phase of the <abbr>MCU</abbr>&mdash;could end like that.</p>

                <p>Time travel reverses that impact. It spits in its face. It makes it an easy undo, a joke. It says, &ldquo;You know that emotion you felt? It wasn&rsquo;t worth having.&rdquo; I love how writer Brett Riley puts it in his article, &ldquo;<a href="http://officialbrettriley.com/2018/02/05/no-more-retcons-and-reboots-rules-for-a-good-comics-universe/">No More Retcons and Reboots: Rules for a Good Comics Universe</a>&rdquo;:</p>

                <blockquote>
                    <p>There were never any stakes&hellip; Nothing was sacred&hellip; If a story touched you, you had best forget it, because the companies sure would&hellip; Why do I want to read stories today that will be meaningless tomorrow? Why do I want to read about characters who might be replaced, killed for a few months, resurrected, killed again, changed beyond recognition only to be changed back again, <i lang="la">ad infinitum</i>, <i lang="la">ad nauseum</i>?</p>
                </blockquote>

                <p>For me, <cite>Endgame</cite>&rsquo;s time travel story undid a lot of the admiration I had for the boldness of <cite>Infinity War</cite>.</p>


                <h2 id="if-not-time-travel-then-what-else">If not time travel, then what else? <a class="dm-dp-anchorLink" href="#if-not-time-travel-then-what-else">#</a></h2>

                <p>When I heard this was the last chapter in this phase of the <abbr>MCU</abbr>, I was hoping that meant everything new. New characters, new storylines, new everything. Then I saw the <a href="https://comicbook.com/marvel/2018/04/13/marvel-cinematic-universe-phase-4-movies-list/">phase 4 movie list</a>:</p>

                <img src="phase4.png" alt="The MCU Phase 4 movie list" />

                <p>While these are exciting movies with some new characters, there are also some old ones too. The conceivable return of Black Widow, Thor, Black Panther, Guardians, Ant-Man, the Wasp, Captain Marvel, Doctor Strange, and Spider-Man doesn&rsquo;t exactly feel new; it still seems like a continuation.</p>

                <p>I like when things end on a high note. Despite my nitpicky time travel quibbble, <cite>Endgame</cite> was a high note. We saw characters like Iron Man way more than characters like Vision, and that&rsquo;s ok; we can still end it there. I think that&rsquo;d be a lot more fitting than getting sick of Scott Lang because there&rsquo;s two more movies with him in it. I&rsquo;d rather leave wanting more of these stories than feeling like it was too much. Let&rsquo;s close this book and move on to a new one. The idea of a multiverse could give us that.</p>

                <p>Let&rsquo;s leave these characters in this universe. They beat Thanos. All the snapped are (regrettably) unsnapped and back. Good on them. The end.</p>

                <p>And let&rsquo;s turn our attention to a different universe for the next decade. One where Kamala Khan is Ms. Marvel instead of Carol Danvers as Captain Marvel. One where Miles Morales is Spider-Man instead of Peter Parker. Even though we saw a graceful transition from a Steve Rogers Captain America to a Sam Wilson Captain America, the multiverse gives us a pass to not need a graceful transition. New place, new stories. &rsquo;Nuff said. </p>

                <h2 id="wrapping-up">Wrapping up <a class="dm-dp-anchorLink" href="#wrapping-up">#</a></h2>

                <p>I hope you enjoyed my little &ldquo;what if?&rdquo; exercise into what <cite>Endgame</cite> could have been. Over a decade with these characters has been more than enjoyable. It&rsquo;s allowed and inspired us to dream up even more new stories, and that&rsquo;s the real success of the <abbr>MCU</abbr>.</p>

