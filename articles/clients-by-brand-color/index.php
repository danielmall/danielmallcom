                <p>After <a href="http://superfriend.ly/">SuperFriendly</a>&rsquo;s first year of business in 2012, I published <a href="http://v3.danielmall.com/articles/superfriendly-annual-report-2012/">an annual report</a> that looked at workload, clients, and tasks at a high level. Visualizing data in aggregate like this has always helped me be intentional what I&rsquo;m currently doing and what I might want to change moving forward.</p>

                <p>2019 will mark year seven for SuperFriendly. As a <a href="https://www.adventist.org/en/">Seventh-day Adventist</a>, I&rsquo;m a big believer in the value of cycles of seven, which often bring completeness and wholeness. It seems like an appropriate time for some more reflection.</p>

