                <p>A few things I noticed when doing this exercise:</p>

                <ul>
                    <li>I&rsquo;ve long followed the mantra that no single client should be responsible for 50% of revenue, but I also apply that in terms of attention. I&rsquo;m pleased to say that that&rsquo;s held through since day 1. The closest we&rsquo;ve gotten (in terms of attention, not revenue) are a 38% client in 2012, a 32% clients in 2013, and a 37% client in 2015.</li>
                    <li>The number of clients per year is slowly dwindling, which I&rsquo;m happy about. We&rsquo;re able to do better work for a smaller number of clients with revenue and profit still increasing. We had 17 clients in 2012, while 2018 wrapped up with 11 clients.</li>
                    <li>Attention seems to be normalizing over the years. In 2012, we had a lot of little projects and one gigantic project. This past year, the distribution seems much more even.</li>
                    <li>Though you wouldn&rsquo;t be able to tell by the scale of these graphs, we&rsquo;re spending less time overall on work. The number of hours we&rsquo;ve put in this past year is about 8% lower than we did in 2012, which makes sense if you compare that with the fact that we worked with 6 less clients. But that also means we&rsquo;re spending a little more time with each client.</li>
                    <li>Revenue is up 51% from 2012, while profit is down 32%. Hmm, that&rsquo;s definitely something to fix for 2019.</li>
                </ul>

                <p>What did you learn about your business over the last year? What more can I tell you about mine?</p>

