                <p>The initial inspiration for this goes to <a href="https://aaronshekey.com/">Aaron Shekey</a >. I saw <a href="https://twitter.com/aaronshekey/status/1042838954532327427">a tweet of his that talked about switching from a built-in webcam to a proper camera</a>, and I was immediately off to research how to do this. Luckily, he wrote <a href="https://aaronshekey.com/posts/video-conferencing-with-a-proper-camera/">a post about his setup</a>, and I&rsquo;m doing most of the things he is, with a few minor modifications. Thanks for all of this, Aaron!</p>

                <p>Here&rsquo;s my desk setup and all the equipment I use:</p>

                <a href="dmall-desk--compressed.png"><img src="dmall-desk--compressed.png" alt="My desk and webcam setup" /></a>        

                <ul>
                    <li><a href="https://amzn.to/2S8G6VY">Olympus OM-D E-M10 Mark II</a></li>
                    <li><a href="https://amzn.to/2SqrVKK">Panasonic Lumix G II 20<abbr title="Millimeter">mm</abbr> f/1.7 ASPH lens</a></li>
                    <li><a href="https://amzn.to/2tGwf01">Elgato Cam Link</a></li>
                    <li><a href="https://amzn.to/2tHRy1p">HDMI to Micro HDMI Cable</a></li>
                    <li>Two spare <a href="https://amzn.to/31CSfp6">Olympus BLS-50 lithium ion batteries</a> and two spare <a href="https://amzn.to/38hQ4Ko">Olympus BCS-5 battery chargers</a></li>
                    <li><a href="https://amzn.to/2H5wHrP">RetiCAM Tabletop Tripod</a></li>
                    <li><a href="https://amzn.to/31Kq9bK">Lume Cube AIR + Computer Mount</a></li>
                </ul>

                <p>I use an Olympus mirrorless camera for my webcam with a pancake lens that has a pretty wide aperture (f/1.7) so I can get some pretty shallow depth of field.</p>
                
                <p>The camera is pretty light&mdash;which is part of the reason I got this particular camera in the first place&mdash;but it&rsquo;s not light enough to sit on top of a monitor like a typical external webcam. So, I use a tabletop tripod to hold it steady, which also has the added benefit of being able to position it at the exact height as well as place on my desk that I want. Since it&rsquo;s next to my laptop where I typically leave my video call screen (usually <a href="https://zoom.us/">Zoom</a> but occasionally <a href="https://hangouts.google.com/">Google Hangouts</a>), it appears to people on the call that I&rsquo;m looking slightly offscreen. I could look directly into the camera, but then I wouldn&rsquo;t be able to see the people I&rsquo;m talking to, which is what I prefer. I may tweak this setup in the future to see if I can achieve both simultaneously.</p>

                <p>The Elgato Cam Link with HDMI to Micro HDMI cable is what makes all of this possible. (If you&rsquo;re using a different camera, be aware that you might need a HDMI to <em>Mini</em> HDMI cable instead of a HDMI to <em>Micro</em> HDMI cable.) Once it&rsquo;s set it, you can switch to this camera as a video input just like you would any other webcam (built into a monitor or external).</p>

                <p>I typically get about 1&frac12; &ndash; 2 hours of straight video time, which is pretty decent. But, since I&rsquo;m on video calls a lot, I always have 2 spare batteries charging for a quick swap in case a battery dies in the middle of a call.</p>

                <p>My studio typically gets a lot of light, so I&rsquo;m rarely in need of additional lighting to make this setup work. However, for the rare evening video calls, I&rsquo;ll switch on a Lume Cube mounted behind my external monitor, which does just the trick.</p>
                
                <p>I leave my camera on manual focus. That means when I first join calls, I have to manually adjust the focus ring on my lens to make sure <a href="http://rising.blackstar.com/for-great-portraits-shoot-the-eyes.html">my eyes are sharply focused</a>. (This is why I always keep &ldquo;Always show video preview dialog when joining a video meeting&rdquo; enabled by default. Letting people see themselves before you see them is just common courtesy.) I tried using autofocus for a while, but the camera didn&rsquo;t always get the focus right, and I spent a disproportionate amount of time on calls trying to reset the focus, distracting me from the what I actually wanted to focus on: the conversation.</p>

