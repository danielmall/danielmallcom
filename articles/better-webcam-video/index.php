                <p>Is all of this worth it? I think so! While some of this is certainly ego and vanity, it&rsquo;s also a form of self-care for me. I think the better I think I look, the more confident I feel. And when I feel good, I act good. Without a doubt, that&rsquo;s worth a couple hundred bucks and a few hours of setup.</p>



