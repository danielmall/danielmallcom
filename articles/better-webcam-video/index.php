                <p><span class="dm-dp-openingLines">In running</span> a <a href="https://superfriendlydesign.systems/">completely distributed agency</a>, I spend a signficant portion of my day on video calls. Like, most days, <a href="https://twitter.com/danmall/status/1214314833698578437">the majority of my day</a>. So, if my colleagues and I have to spend a lot of time looking at my mug, I might as well try to make that a more pleasant experience for everyone involved.</p>

                <div class="dm-c-carbonWrap">
                    <script async type="text/javascript" src="//cdn.carbonads.com/carbon.js?zoneid=1696&amp;serve=CVYD42T&amp;placement=danielmallcom" id="_carbonads_js"></script>
