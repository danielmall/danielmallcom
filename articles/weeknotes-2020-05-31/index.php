                <h2 id="personal">Personal <a class="dm-dp-anchorLink" href="#personal">#</a></h2>

                <ol>
                    <li>
                        The world erupted this week with protests and riots over the murder of George Floyd by police officers, another death in a long string of police brutality against Black people. I&rsquo;m still processing what I can do to contributing to making it so that this never happens again. I know I can start by condemning this and speaking out against racism, and I&rsquo;m to put processes in place that make sure it doesn&rsquo;t just end with that for me. Some specific steps I&rsquo;m trying:
                        <ul>
                            <li>I made it a personal policy to only retweet Black people this week (probably going to do this for longer)</li>
                            <li>Withdrawing from and not agreeing to speak at conferences that aren&rsquo;t committed to inclusion specifically for Black people and showing it by having as many Black speakers as any other group represented.</li>
                            <li>Some major changes at SuperFriendly (more details to come)</li>
                        </ul>
                    </li>
                    <li>This is also the week that our county moves to yellow phase of our governor&rsquo;s COVID-19 reopening plan. Our neighbors across the street had a big birthday party. I&rsquo;m approaching all of this way more conservatively and still trying to quarantine and wear masks outside as much as possible, while balancing that with still easing into re-integrating with the rest of society.</li>
                    <li>Replaced old bike tires with new ones myself in order to go bike riding as a family. This sounds like an insignificant point, but as someone who is <em>not</em> handy and is uncomfortable around even simple tools like wrenches, I&rsquo;m incredibly proud of this.</li>
                    <li>Slept an average of 4 hours and 48 minutes per night. Average bedtime: 10:46pm. Average wake up time: 5:07am. I typically sleep pretty well, and I&rsquo;m not often aware of whether or not I&rsquo;m stressed. My lower than average sleep amount is one way I can realize that something&rsquo;s not right with me.</li>
                </ol>

