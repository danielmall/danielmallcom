            <h1 class="dm-c-pageHeader_title">
                My Experience with Laser Eye&nbsp;Surgery
            </h1>

            <h2 class="dm-c-pageHeader_date">February 22, 2019 at 4:12 <abbr title="Post Meridien">PM</abbr></h2>

        </header><!-- .page-header -->

        <main role="main" class="dm-c-articleWell">

            <div class="dm-dp-textPassage dm-l-articleGrid--main">

                <p><span class="dm-dp-openingLines">I started wearing glasses</span> in 1995, and I switched to contacts in 1999. Two days ago&mdash;twenty years later&mdash;I got laser eye surgery. Here&rsquo;s the play-by-play.</p>

                <div class="dm-c-carbonWrap">
                    <script async type="text/javascript" src="//cdn.carbonads.com/carbon.js?zoneid=1696&amp;serve=CVYD42T&amp;placement=danielmallcom" id="_carbonads_js"></script>
                </div><!-- .dm-c-carbonWrap -->

                <p>In my <a href="/articles/2018-year-in-review/">2018 Year in Review</a>, I mentioned wanting to investigate being done with glasses and contacts for good. So a month ago, I quickly Google-search for the closest laser eye surgery place<sup class="dm-c-sidenotes--link"><a href="#note1">1</a></sup> and go in for a consultation. After a brief eye exam, they tell me that my -2.5 left eye prescription and -3.25 right eye prescription make me eligible for the procedure.</p>

                <aside id="note1" class="dm-c-sidenotes dm-l-articleGrid--sidenote">

                    <p>This article is more about my experience than it is a review of the LASIK office I got the surgery done. If you&rsquo;re curious about how I picked, it was mostly based on a combination what was close by and reading great reviews on Google and Facebook.</p>

                </aside>

                <p>Eyes naturally change over time, so apparently about 5% of patients need an enhancement after a few years. They give me two different prices: $3,902 for the surgery with a lifetime plan that covers enhancements for life and $2,611 that only covers enhancements for 2 years after the initial surgery. They also offer a $200 incentive on each plan if I do the procedure before the end of the month.</p>

                <p>I don&rsquo;t have enough time to do the procedure and recover before some upcoming travel, so I put it off a few weeks, which gives me some additional time to think about it. <a href="https://abookapart.com/products/pricing-design">Knowing a little bit about how people set prices</a>, the $200 incentive clues me in to the possibility that they might need the business more than I want to have unassisted perfect vision. A month later, I call back to schedule the procedure, and I tell them that I&rsquo;d love to do the lifetime plan but that I don&rsquo;t really want to spend more than $3,000 on this.<sup class="dm-c-sidenotes--link"><a href="#note2">2</a></sup> After a brief hold, they say that, in addition to the original $200 discount they offered, they can do an additional $500 off for local customers, bringing the total to $3,202. I accept their clearly-fabricated but still-gracious additional discount and pick a date for the procedure.</p>

                <aside id="note2" class="dm-c-sidenotes dm-l-articleGrid--sidenote">
                    <p>They offered financing too, but I&rsquo;m generally not a fan of that. I&rsquo;m not very disciplined when it comes to using credit, so I&rsquo;ll often choose to wait until I have the money to pay for what I want in full.</p>
                </aside>

                <h2 id="surgery-time">Surgery time <a class="dm-dp-anchorLink" href="#surgery-time">#</a></h2>

                <p>They call in a few eye drop prescriptions to my pharmacy and tell me not to wear contacts for at least 3 days before the procedure. Eye drops in bag and glasses on my face, Em and the kids ride with me to the LASIK office. I arrive around 1:30<abbr title="Post Meridien">PM</abbr>. I fill out some paperwork, pay, and schedule a follow up appointment for the next day. I ask if I&rsquo;ll be able to drive myself, to which they answer a resounding, &ldquo;Absolutely.&rdquo;</p>
                
                <p>I sit for about an hour, then the doctor calls me in. He says, &ldquo;Time to take your glasses off for the last time&rdquo;&mdash;a bit melodramatic but I&rsquo;m lovin&rsquo; it&mdash;and administers a few numbing drops.</p>

                <p>While waiting for the drops to take effect, he asks me about my left eyelid, which droops down a little lower than my right. He asks me if it had always been that way, which I confirm. He mentions it won&rsquo;t be a problem at all, but that if it affects my vision in the future, I might want to get it lifted. I silently acquiesce, as he&rsquo;s not the first doctor to mention that to me.</p>

                <p>He leads me into the operating room. An assistant asks me to lie down, and she administers more numbing drops (which I&rsquo;ve read apparently reduces your urge to blink). I&rsquo;m not positive of what happens after that since I don&rsquo;t actually see it myself, but I&rsquo;ll tell you what it feels like. <sup class="dm-c-sidenotes--link"><a href="#note3">3</a></sup></p>
                
                <aside id="note3" class="dm-c-sidenotes dm-l-articleGrid--sidenote">
                    <p>I confirmed a lot of what I thought was happening with <a href="https://www.youtube.com/watch?v=aDWaKpk8Ygs">Louis Cole&rsquo;s laser eye surgery vlog</a>. Don&rsquo;t watch if you&rsquo;re squeamish.</p>
                </aside>
                    
                <p>The assistant tapes my right eyelids open. The doctor applies some tool that locked the eyelids into place; I feel a little pressure on my right eye but it&rsquo;s ever-so slight. The doctor swivels a machine on top of my right eye, and I stare up at a red dot. I have both eyes open, and the doctor says, &ldquo;Your right eye is going to go dark,&rdquo; and suddenly, it does. This is the part where they peel back my cornea to get ready to do the correction <sup class="dm-c-sidenotes--link"><a href="#note4">4</a></sup>, but I don&rsquo;t feel anything here.</p>

                <aside id="note4" class="dm-c-sidenotes dm-l-articleGrid--sidenote">
                    <p>If you want to know how laser eye surgery actually works, here&rsquo;s <a href="https://www.allaboutvision.com/visionsurgery/lasik.htm">a complete guide</a>.</p>
                </aside>

                <p>The same happens with my left eye; again, I don&rsquo;t feel anything.</p>

                <p>The assistant asks me to get up slowly as she holds my hand. My vision is blurry. She slowly leads me to another station 3 feet away and asks me to lie down again.</p>

                <p>The doctor swivels a new machine over my right eye. I remember less about the details of this one. I look up and see red lights in a cross-stitch pattern surrounded by green lights in the same pattern. I assume this is the laser that&rsquo;s going to do the correction. The doctor seems to have a tool and the best way I can describe it was like he&rsquo;s &ldquo;fishing around&rdquo; in my right eye. I don&rsquo;t feel this&hellip; more like I could see it, albeit blurry. Next, the laser seems to be doing something, and I smell a slight burning (I previously read about this, so I expected it). Doctor comes back with his fishing-around tool and then says the right eye is done. That wasn&rsquo;t so bad at all!</p>

                <p>Next up: left eye. Doctor fishes around again, but this time, I feel it a bit. It feels like something grazing my eye. Laser does it&rsquo;s thing, then Doctor says something to Assistant&mdash;I can&rsquo;t quite make it out, but I think I hear the word &ldquo;scratch,&rdquo; which worries me a bit&mdash;then Doctor fishes around again. But rather than being done now like the right eye was, the laser goes for another round. That worries me too, because that&rsquo;s not what happened with the right eye. Doctor comes back to fish around, and tells me the left eye is done.</p>

                <p>Doctor and Assistant help me slowly stand up, and Doctor says, &ldquo;You did a great job.&rdquo; This worries me even more, because he sounds like he&rsquo;s trying to convince himself as much as me.</p>

                <p>I ask, &ldquo;Did everything go ok?&rdquo;</p>
                
                <p>Doctor replies, &ldquo;Oh yes, everything&rsquo;s great. Go home and take a nap.&rdquo;</p>

                <p>My vision is blurry at this point. I grab my stuff, throw some sunglasses on, and am escorted out to my car.</p>
                

                <h2 id="the-drive-home">The drive home <a class="dm-dp-anchorLink" href="#the-drive-home">#</a></h2>
                
                <p>Em drives me home. I&rsquo;m reclined in the passenger seat, and the kids are in the backseat.</p>

                <p>I have my eyes closed, and I think the numbing drops are wearing off. Tears start to stream down my face, uncontrollably. I&rsquo;m not crying; at least I don&rsquo;t think so, but it feels like it, both physically and emotionally. <strong>IT. BURNS.</strong> My nose is running. When I keep my eyes closed, it burns. When I try to open my eyes, tears flood down my face. I can&rsquo;t keep them open for long anyway, so I close them again, which burns, so I want to open them. The cycle continues.</p>

                <p>I&rsquo;m not allowed to rub or even touch my eyes&mdash;ever, apparently&mdash;so my beard and top of my shirt are getting damp from my tears. I try to fall asleep, but it burns too much. I fall asleep a few minutes before we pull into the driveway.</p>

                <p>Em helps me out of the car. My eyes closed, she leads me into our room, where I settle into bed around 4<abbr>PM</abbr>.</p>



                <h2 id="naptime">Naptime <a class="dm-dp-anchorLink" href="#naptime">#</a></h2>

                <p>Official instructions are to nap for 3 &ndash; 6 hours after the procedure, because there&rsquo;s really nothing else you could do anyway. The night before, I stayed up late the night and woke up early so I&rsquo;d be tired enough to nap. The LASIK office recommended Tylenol PM&mdash;or Valium&mdash;but I&rsquo;m usually a good sleeper, so I decide to go cold turkey.</p>

                <p>Em helps me carefully tape sleep shields on my eyes so I wouldn&rsquo;t accidentally rub my eyes in my sleep. She put a glass of water on my nightstand and leaves me so I can try to get some rest. I start listening to a boring finance audiobook. Unfortunately, my eyes are still burning, which is keeping me awake. Also, the boring finance audiobook&mdash;<cite><a href="https://yourmoneyoryourlife.com/book/">Your Money or Your Life</a></cite>&mdash;turns out to be really interesting, so I&rsquo;m actually paying attention instead of zoning out.</p>

                <p>After what seems like an hour, I switch to a <a href="https://open.spotify.com/user/spotify/playlist/37i9dQZF1DX8Uebhn9wzrS?si=ztcBX52uQUSj6euJI4_t2Q">Chill Lofi Study Beats Spotify playlist</a> on low volume and finally start to doze off.</p>

                <p>I wake up every 30 minutes or so. My eyes are still burning. I try to open my eyes, and tears flood out. I close them again and fall back asleep.</p>


                <h2 id="after-nap">After nap <a class="dm-dp-anchorLink" href="#after-nap">#</a></h2>

                <p>I wake up more fully about two-and-a-half hours later at 6:30<abbr>PM</abbr>, mostly because I&rsquo;m hungry. Eyes still burning, I open them; tears still flood out, but seems like less tears than before. I force myself to keep my eyes open as long as I can before shutting them again. I sit in the dark for about half-an-hour, training myself to open and close my eyes as normally as possible.</p>

                <p>I start the recommended eye drop routine: a steroid eyedrop and an antibiotic one every 2 hours. I eventually feel stable enough to throw some sunglasses on and venture out into the kitchen. Em and the kids have kindly turned of most of the lights in the house, and they&rsquo;re hanging out in the basement so I&rsquo;d have a quiet upstairs all to myself. I look slowly toward a light, then stop myself, as my eyes seem too sensitive for that. I warm up a bowl of leftover pasta and eat it in the dark dining room, mostly to get used to being awake. My vision seems clear, but I do see some spots and too much light doesn&rsquo;t feel great. My eyes still sting, especially my left eye, which feels scratched (though I can&rsquo;t tell if that&rsquo;s just the power of suggestion from my worries about the procedure).</p>

                <p>Em and the kids come upstairs to read stories, pray, and get to bed. I say goodnight to the kids, and hang out in bed with Em. We talk for a while, mostly about my surprisingly interesting finance book. An hour or two of engaging conversation later, I realize I haven&rsquo;t thought about my eyes once, and that, now that I am thinking about them, they don&rsquo;t sting as much. One of a many benefits to having a riveting wife.</p>

                <p>Even though the pain is decreasing, I still can&rsquo;t really look at light without wincing. I can&rsquo;t fathom how I&rsquo;m supposed to drive myself to a follow-up appt just 12 hours from now. I do my final round of eye drops for the night, tape my sleep shields on, and get ready for bed. I took a nap just a few hours ago, but I&rsquo;m still tired from the tenseness of it all, so I&rsquo;m looking forward to sleep.</p>

                
                
                
                <h2 id="the-next-morning">The next morning <a class="dm-dp-anchorLink" href="#the-next-morning">#</a></h2>

                <p>I intentionally wake up at 5:30<abbr title="Ante Meridien">AM</abbr> (just about my usual waketime) because I want a few hours to get acclimated before having to drive to a 9<abbr>AM</abbr> follow-up appointment. My vision is blurry, but largely because I still have the sleep shields on. I walk to the bathroom, take off the sleep shields, and look around. <strong>Things are pretty clear.</strong> Also, almost no pain at all. Wow.</p>

                <p>I turn the light on but brace myself. No light sensitivity.</p>

                <p>I walk around the house. Life looks pretty good.</p>

                <p>My care instructions say I should wear sunglasses indoors for a week, not because of light sensitivity, but as protection for my eyes so they don&rsquo;t accidentally get bummed or so I don&rsquo;t unintentionally rub them. I pop on sunglasses, marveling at the obvious difference from feeling so rough the night before and so good now.</p>

                <p>I drive myself to my follow-up appointment with no issues. They test my eyesight: <strong>20/15 in each eye, 20/20 together,</strong> just 18 hours after the procedure.</p>

                <p>We&rsquo;re seriously living in the future.</p>



                <h2 id="two-days-in">Two days in <a class="dm-dp-anchorLink" href="#two-days-in">#</a></h2>

                <p>I&rsquo;m writing this post on two day after the procedure. I&rsquo;m following the eye drops routine pretty religiously, as I&rsquo;ve read that sticking to that increases the speed of recovery significantly. If my eyes itch, I&rsquo;m supposed to use artificial tears, as itchiness is a symptom of dryness. The LASIK office suggested that I use artificial tears as often as I want, so I&rsquo;m making liberal use of that. The worst of it is that my left eye still feels <em>slightly</em> sandy every few hours, kinda like I have a dirty contact lens in. But, it goes away immediately when I use the eyedrops.</p>

                <p>I switched to sleeping in ski goggles last night instead of sleep shields, as it&rsquo;s a little more comfortable and still keeps me from accidentally rubbing my eyes. And my co-workers seem to be fine with me joining calls with video either turned off or turn on while I&rsquo;m wearing sunglasses.</p>


                <h2 id="life-changing">Life changing <a class="dm-dp-anchorLink" href="#life-changing">#</a></h2>

                <p>The biggest thing I heard from people who&rsquo;ve had this done previously is that it was life-changing to be able to wake up and immediately see clearly. I didn&rsquo;t really experience that this morning, partly because I&rsquo;ve heard it so much that I think it got over-hyped and partly because I&rsquo;ve fallen asleep with contacts enough times to have experienced and remembered waking up with perfect vision.</p>

                <p>What&rsquo;s been more impactful for me so far is where I realize my previous habits and aren&rsquo;t necessary anymore. I don&rsquo;t have to take my contacts out before bed. I don&rsquo;t have to pack 2 weeks&rsquo; worth of extra contacts in a suitcase when I travel just in case I get stuck somewhere. I don&rsquo;t have to constantly carry around microfiber cloths to wipe dirty glasses. I don&rsquo;t have to worry about getting a contact knocked in my weekly basketball game. These are tiny things, but I think they&rsquo;re what I&rsquo;ll enjoy most.</p>

                

