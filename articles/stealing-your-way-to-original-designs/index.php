        <h1 class="dm-c-pageHeader_title">
            Stealing Your Way to Original Designs
        </h1>

        <h2 class="dm-c-pageHeader_date">July 3, 2017 at 6:01 <abbr title="Ante Meridien">AM</abbr></h2>

    </header><!-- .page-header -->

    <main role="main" class="dm-c-articleWell">

        <div class="dm-dp-textPassage dm-l-articleGrid--main">

            <p><span class="dm-dp-openingLines">I have a framework</span> that I find very helpful in teaching <a href="http://superfriend.ly/academy/">my apprentices</a> how to design. It&rsquo;s 3 steps:</p>

            <div class="dm-c-carbonWrap">
                <script async type="text/javascript" src="//cdn.carbonads.com/carbon.js?zoneid=1696&amp;serve=CVYD42T&amp;placement=danielmallcom" id="_carbonads_js"></script>
            </div><!-- .dm-c-carbonWrap -->

            <ol>
                <li><a href="#imitate">Imitate</a></li>
                <li><a href="#remix">Remix</a></li>
                <li><a href="#invent">Invent</a></li>
            </ol>

            <p>Here are the details.</p>

            <h2 id="imitate">1. Imitate <a class="dm-dp-anchorLink" href="#imitate">#</a></h2>

            <p>The first step to being just a competent designer—or a competent anything, for that matter—is doing what someone else can do. It&rsquo;s a test: can you follow directions? Except, instead of following directions you read or hear, you follow directions that you see. </p>

            <p>It&rsquo;s no coincidence that junior designers often get trained up by doing production work after an art director has set an art direction. If the art director makes the main headline 48px bold Helvetica with 16px of margin on each side, do you do the same on every screen you design? Sounds simple, but many junior designers struggle to even get this down. Why? Many want to be chefs but aren&rsquo;t willing to be line cooks first. It&rsquo;s not glamorous work, but it builds design muscles, muscles that you&rsquo;ll need in order to get better and advance to the next step. </p>

            <p>Think of it like learning to draw by tracing. In fact, it’s literally the same thing. The first few assignments of my designers&rsquo; apprenticeships are taking screenshots of existing websites, pasting them into Sketch or Photoshop, dropping the opacity and locking the layer, and tracing over it to produce the same layout, colors, illustrations, typography, and everything else needed to reproduce the site pixel for pixel.</p>

            <p>In his article <cite><a href="https://www.smashingmagazine.com/2017/02/improving-ui-design-skills-copywork/">Copy If You Can: Improving Your UI Design Skills With Copywork</a></cite>, Erik Kennedy extols the virtues of copywork as a great way to practice and hone <abbr title="User Interface">UI</abbr> design skills. Even more, it gets you out of going to your usual bag of tricks and instead gives you new ones to add.</p>

            <blockquote>
                <p>Done right, copywork exposes you to design decisions you simply wouldn&rsquo;t have made on your own.</p>
                <address>Erik Kennedy</address>
            </blockquote> 

            

            <p>Whether new to design or a seasoned creative director, this simple exercise will get you warmed up, like stretching before a long run.</p>

            <h2 id="remix">2. Remix <a class="dm-dp-anchorLink" href="#remix">#</a></h2>

            <p>Once you can confidently imitate any style you come across, you can start to tweak the elements to feel more custom to the way you need to use it. This step has the least parameters and needs the least guidelines, because it&rsquo;s up to your imagination and the amount of changes you&rsquo;re willing to try.</p>

            <p>I&rsquo;m a firm believer in the idea that <a href="https://www.biblegateway.com/passage/?search=Ecclesiastes%201:9">there&rsquo;s nothing new under the sun</a>. Said differently, <a href="http://everythingisaremix.info/watch-the-series/">everything is a remix</a>. Freed from the pressure that all things designers do need to be original works of art, I can instead focus my efforts on what sources to combine in a unique way that my audience is less accustomed to.</p>

            <h2 id="invent">3: Invent <a class="dm-dp-anchorLink" href="#invent">#</a></h2>

            <p>Pablo Picasso is well known for his original Cubist work, but most people don&rsquo;t know that he was an excellent classical artist first. He could draw and paint photorealism as well as any of the great masters, but it wasn&rsquo;t until he got that down that he ventured into his own unknown stylistic territory.</p>

            <p>In his Wall Street Journal article, <cite><a href="https://www.wsj.com/articles/SB10001424052748703989304575503730101860838">The Genius of the Tinkerer</a></cite>, author Steven Johnson describes a concept he calls <em>the adjacent possible</em>. He tells the story of a young orthopedic surgeon who, while diving in the Great Barrier Reef, observes a species of coral that seem to be able to heal itself as it breaks. Years later, while operating on a patient, he recalls this coral and its amazing bonding powers. Several years and patents later, his method is the leading way that orthopedic surgeons around the world help their patients mend their broken bones.</p>

            <p>The adjacent possible is about taking two seemingly unconnected ideas and metaphorically placing them next to each other to see how they might work together. Clients often hire me to invent unique and original art directions for them. I can deliver on this by using the adjacent possible to steal my way to something original.</p>

            <blockquote>
                <p>The strange and beautiful truth about the adjacent possible is that its boundaries grow as you explore them. Each new combination opens up the possibility of other new combinations.</p>
                <address>Steven Johnson</address>
            </blockquote>

            <p>Let&rsquo;s look at how this works in practice. </p>

            <section class="dm-dp-asteriskSplit">

                <p>Say SpaceZzzz&trade; hired me to create a site to advertise a new service they&rsquo;re offering: rental properties on different planets. They&rsquo;d get you there, you stay as long as you&rsquo;d like, and they&rsquo;ll get you back. Think <a href="https://www.uber.com/">Uber</a> meets <a href="https://www.airbnb.com/">Airbnb</a> meets <a href="http://www.imdb.com/title/tt0816692/">Interstellar</a>. </p>

                <p>The first thing I&rsquo;ll do is make a list of all of the types of content I&rsquo;ll need on the homepage. After talking to stakeholders and potential users in the target market, perhaps I&rsquo;d arrive at 5 different sections:</p>

                <ol>
                    <li>An elevator pitch about the new service </li>
                    <li>A featured destination (or a few features)</li>
                    <li>Some details about what people may be anxious about, like long travel or planetary accommodations </li>
                    <li>Price </li>
                    <li>Contact details </li>
                </ol>

                <p>To start, I&rsquo;ll block out these main sections with a few <a href="http://v3.jasonsantamaria.com/archive/2004/05/24/grey_box_method.php">gray boxes</a>.</p>

            </section>

        </div>


        <div class="dm-c-articleImage dm-u-clearfix dm-u-background--gray">

            <a href="gray-boxes.png"><img src="gray-boxes.png" alt="Gray boxes for page sections" /></a>
           
