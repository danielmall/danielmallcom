            <h1 class="dm-c-pageHeader_title">
                A Syllabus for Web Design &amp; Web Development Apprenticeships
            </h1>

            <h2 class="dm-c-pageHeader_date">May 26, 2020 at 7:10 <abbr title="Ante Meridien">AM</abbr></h2>

        </header><!-- .page-header -->

        <main role="main" class="dm-c-articleWell">

            <div class="dm-dp-textPassage dm-l-articleGrid--main">
            
                <p><span class="dm-dp-openingLines">From 2012&ndash;2019</span>, I ran a 9-month apprenticeship at <a href="https://superfriendlydesign.systems/">SuperFriendly</a> for people new to design and development that wanted to make their way into the tech industry. I&rsquo;ve previously written about <a href="http://v3.danielmall.com/articles/apprenticeships-superfriendly-academy/"><em>why</em> apprenticeships are valuable</a> and <a href="http://v3.danielmall.com/articles/apprenticeships-superfriendly-academy/#academy"><em>what</em> format I used</a>; here are the details about <em>how</em> I ran them.</p>

                <div class="dm-c-carbonWrap">
                    <script async type="text/javascript" src="//cdn.carbonads.com/carbon.js?zoneid=1696&amp;serve=CVYD42T&amp;placement=danielmallcom" id="_carbonads_js"></script>
                </div><!-- .dm-c-carbonWrap -->

                <p>Within that 7-year span, I had almost 20 apprentices. Some finished the apprenticeship; some didn&rsquo;t. Some got full-time jobs in the discipline they pursued during their apprenticeship. Some took the skills they learned in their apprenticeship and applied them to a tangential industry. Some went freelance or started their own companies. Some went back to doing what they did before the apprenticeship.</p>

                <p>As I write this information for the first time, I&rsquo;m realizing in hindsight how valuable documenting this earlier would have been for me and even moreso for my apprentices. I&rsquo;m partially writing all of this down as a resource for myself in case I ever bring the apprenticeship back again.</p>
                
                <p lang="fr">C&lsquo;est la vie.</p> 

                <p>The final 3 months of my apprenticeship focused on preparing the apprentice for a job elsewhere, so, in terms of describing a replicable format, I&rsquo;ll focus the syllabus specifically on the first 6 months (180 days).</p>
                
                <div class="dm-dp-asteriskSplit">

                    <h2 id="objective">Objective <a class="dm-dp-anchorLink" href="#objective">#</a></h2>
                    
                    <p>To unlock a new way of life for the apprentice through emergently gaining knowledge of web design and/or web development and the accompanying industry.</p>

                </div>
                
                <div class="dm-dp-asteriskSplit">

                    <h2 id="prerequisites">Prerequisites <a class="dm-dp-anchorLink" href="#prerequisites">#</a></h2>

                    <ul>
                        <li><strong>Computer proficiency.</strong> As little prior knowledge about design and development is preferred, as most of it would likely have to be untaught. If you can already make a design or development portfolio, you&rsquo;re over-qualified for this apprenticeship.</li>
                        <li>Because this apprenticeship focuses on those <em>interested</em> in getting into tech, I host a several-week <strong>prelude</strong> to the apprenticeship. I ask each prospective apprentice to come to the office for 2&ndash;4 hours per day. I assign various reading and watching, from books to online articles to conference talks to YouTube videos. I also encourage them to observe the work that&rsquo;s currently happening in the office and to take notes on words and acronyms that sound unfamiliar, and we spend time discussing what&rsquo;s happening and why. If this is going to be a full-time career, I want the apprentice to have the chance to see what that could look like and try to envision themselves in that role. For many apprentices, it will be their first time in a desk job and/or sitting in front of a computer for an extended amount of time.</li>
                    </ul>

                </div>

                
                <div class="dm-dp-asteriskSplit">

                    <h2 id="required-materials">Required Materials <a class="dm-dp-anchorLink" href="#required-materials">#</a></h2>

                    <ul>
                        <li>Laptop</li>
                    </ul>

                </div>


                
                <div class="dm-dp-asteriskSplit">

                    <h2 id="attendance-policy">Attendance Policy <a class="dm-dp-anchorLink" href="#attendance-policy">#</a></h2>

                    <p>At the beginning of the apprenticeship, in-person attendance at SuperFriendly <abbr title="Headquarters">HQ</abbr> 4&ndash;5 days a week from 10<abbr title="Ante meridien">am</abbr>&ndash;4<abbr title="Post meridien">pm</abbr> is required. During the second and third units of the apprenticeship, in-person attendance can decrease and independent remote work can increase.</p>

                    <p>(Because so much of the learning comes from passive observation, reflection, and real-time discussion, I haven&rsquo;t yet figured out how to make a remote apprenticeship work as well as an in-person one, so to date it has never been an option that I offered.)</p>

                </div>


                <div class="dm-dp-asteriskSplit">

                    <h2 id="methodology">Methodology <a class="dm-dp-anchorLink" href="#methodology">#</a></h2>
                    
                    <p>Every assignment comes with a conversation covering these kinds of questions:</p>

                    <ul>
                        <li>What was easy?</li>
                        <li>What was difficult?</li>
                        <li>What did you learn?</li>
                    </ul>

                    <p>The answers to these questions help shape the individual learning and direction each apprenticeship takes.</p>

                </div>

                
