                <p><span class="dm-dp-openingLines">Over a year ago, Vitaly Friedman</span> from Smashing Magazine asked me if I&rsquo;d be game to do a conference talk without slides, something more about showing work in action.</p>

                <img src="smashing-live.png" alt="The initial message from Vitaly Friedman about a &ldquo;no slides&rdquo; conference" />

                <div class="dm-c-carbonWrap">
                    <script async type="text/javascript" src="//cdn.carbonads.com/carbon.js?zoneid=1696&amp;serve=CVYD42T&amp;placement=danielmallcom" id="_carbonads_js"></script>
                </div><!-- .dm-c-carbonWrap -->

                <p>I was immediately excited to do it, as I could immediately think of how I&rsquo;d do that, since I do that almost daily with <a href="http://v3.danielmall.com/articles/apprenticeships-superfriendly-academy/">my apprentices</a>. (I previously wrote down the details of that framework in <a href="/articles/stealing-your-way-to-original-designs/">Stealing Your Way to Original Designs</a>.)</p>

                <p>I normally don&rsquo;t stress too much about talks nowadays as I&rsquo;ve given <a href="/speaking-podcasts/">my fair share</a> of them, but this one was a bit different. Trying to do final prep the night before was difficult, because I didn&rsquo;t have slides to review. Also, because of the format of the talk, I didn&rsquo;t <em>want</em> to prep extensively. Too much prep might dilute the whole concept of showing how I make decisions while designing.</p>

                <p>So I decided the best prep would probably to get the best night&rsquo;s sleep I could get.</p>

                <p>Yesterday, I gave that talk.</p>

                <p>I planned to redesign the <abbr title="Refugee and Immigrant Center for Education and Legal Services"><a href="https://www.raicestexas.org/">RAICES</a></abbr> site on stage. In my rough rehearsals, I designed a fair bit of the site. On stage, all I got to was buttons. ðŸ˜¶</p>

                <p>I walked off stage uneasy about the talk, and felt uneasy about it all day. One thing I&rsquo;m most proud of is my talks is feeling like like share <em>a lot</em>. With this one, I don&rsquo;t think I shared enough. The feedback I got from attendees was all nice, but I also know that people who hated it probably wouldn&rsquo;t come up to me to say so.</p>

                <p>After sleeping on it though, now I feel great about the talk. Two pieces of feedback I got that stand out:</p>

                <ol>
                    <li>A few designers felt validated in their processes by seeing mine.</li>
                    <li>A few developers said design felt less intimidating now, both to understand as well as to try.</li>
                </ol>

                <p>As a speaker, that&rsquo;s more than a great outcome. Upon reflection, getting an outcome like that without spending weeks and months laboring over crafting the &ldquo;perfect&rdquo; narrative for just the right sequence and quantity of slides is wonderful!</p>

                <p>I tend to view my talks as a performance. And I think there&rsquo;s merit to that. But I hadn&rsquo;t considered much that there&rsquo;s another format that can achieve similar outcomes. In the language of <a href="https://twitter.com/blairenns">Blair Enns</a>&rsquo; <cite><a href="https://amzn.to/2N0eiyi">Win Without Pitching Manifesto</a></cite>, I could &ldquo;replace presentations with conversations.&rdquo; I do that almost completely in client work but not for conferences.</p>

                <p>I didn&rsquo;t expect this, but I&rsquo;m now seriously considering this format as a way I do talks at other conferences. (I&rsquo;d love feedback about that from other conference organizers, if you have opinions.)</p>

                <p>All this is to say: thanks to Vitaly and the Smashing Magazine and Smashing Conference crew for challenging the typical format. I learned a lot about how I can be a better speaker.</p>

