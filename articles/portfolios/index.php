            <h2 id="template-for-great-portfolio">A template for a great portfolio <a class="dm-dp-anchorLink" href="#template-for-great-portfolio">#</a></h2>

            <p>In evaluating potential candidates, many hiring managers have criteria that looks something like this:</p>

            <ul>
                <li><strong>Does the candidate have the skills to succeed at this job?</strong> For example, can they already write Rails code for apps that millions of people use simultaneously? Or, can they take a concept from a whiteboard sketch to a polished interface design without much help from others?</li>
                <li><strong>Can the candidate prove that they have the skills to succeed at this job?</strong></li>
                <li><strong>If they don&rsquo;t already have the skills, how long would it take to learn them?</strong> Each organization has its own tolerance for this. Organizations under stakeholder and/or shareholder pressure may want you to get up to speed in a few weeks, while others may be willing to give you up to a year to get your bearings. Beware: this tolerance is rarely discussed internally or even acknowledged. It&rsquo;s often &ldquo;just a feeling&rdquo; that the hiring manager may have.</li>
                <li><strong>How well would the candidate fit in here?</strong> Are they like everyone else on the team? If not, is the difference one that will make everyone here better or make everyone here feel awkward?</li>
            </ul>

            <p>Though the specific criteria will likely vary, it can often be summarized like this: <strong>how easily can I already see this person working here?</strong> A great portfolio answers that question as clearly as possible <em>before</em> any in-person interviews.</p>

            <p>What could that portfolio look like?</p>

            <p>Let&rsquo;s say I wanted to apply for this <a href="https://jobs.nike.com/job/beaverton/senior-designer-north-america-retail-experience-design/824/9118910">Senior Designer, North America Retail Experience Design position</a> at Nike. First, I&rsquo;d find the person or people who may be reviewing this, and I&rsquo;d address it to them. For this job, I might address it to <a href="https://www.linkedin.com/in/otake/">Junichi Otake</a>, Experience Design Director Retail Innovation at Nike. I don&rsquo;t know him personally, but I have 50 mutual connections with him on LinkedIn, so perhaps I could have someone who knows us both introduce us.</p>

            <p>As far as the content of that portfolio goes, here&rsquo;s a rough wireframe of what that might be on there:</p>

            <img src="portfolio-template.svg" alt="A portfolio template" />

            <p>The important bits:</p>

            <ol>
                <li><strong>An opening statement,</strong> addressed to a particular person, introducing myself and stating clearly why I&rsquo;d be a great fit for the position.</li>
                <li><strong>A collection of my work,</strong> specifically tailored to speak to this particular job description. For example, this job description uses specific phrases like &ldquo;product presentation&ldquo; and &ldquo;retail business targets,&rdquo; so I used those same phrases in talking about my work.</li>
                <li><strong>Something special about me</strong> professionally or personally that sets me above everyone else applying for this job. I&rsquo;m not just another designer; I&rsquo;m a designer that&rsquo;s also a sneakerhead.</li>
            </ol>

            <p>Remember: this is a love letter to your dream job, so pour as much of yourself into it as you can! As you can probably tell, I used the most vanilla layout I could for this example, but the finished version is another opportunity to show you&rsquo;re right for the company. Bonus points for:</p>

            <ul>
                <li>Using the same/similar typefaces and color scheme as the company</li>
                <li>Referencing some extra research you did, like info from a podcast or a trade magazine</li>
                <li>Pictures of you attending that company&rsquo;s events, evidence of you being a longtime customer, or other things that may distinguish you from other candidates</li>
            </ul>

            <p>The intention here is to answer some of the questions the hiring manager might have before they even ask it. For example, they may think, &ldquo;How would Dan would handle working on Nike+, which has both digital and physical components?&ldquo; I answer that&mdash;in advance, in my portfolio, before they actually ask me&mdash;in my portfolio piece for Crayola, which has both digital and physical components. They might wonder, &ldquo;Would Dan work well on a large team like ours?&rdquo; Which I answer in portfolio pieces about working with different team sizes. They might be curious as to whether or not I&rsquo;d even like working there, to which my sneaker fetish callouts would answer that it&rsquo;d be a slice of heaven for me. You get the idea.</p>

            <h2 id="reframing-portfolios">Reframing portfolios (even if only in your own mind) <a class="dm-dp-anchorLink" href="#reframing-portfolios">#</a></h2>

            <p>(<em>Note: this section was added September 7, 2018, five days after the article&rsquo;s initial publishing.</em>)</p>

            <p>In response to this article, design Fedor Shkliarau <a href="https://twitter.com/shkliarau/status/1037003567608066048" title="A Twitter conversation between Fedor Shkliarau and Dan Mall">pointed out</a> that this may be more of a cover letter than a portfolio. Through that discussion, we arrived at the best term for something like this is <strong>a proposal</strong>. After I <a href="http://v3.danielmall.com/articles/prequalifying-clients/">prequalify clients</a> for <a href="http://superfriend.ly/">SuperFriendly</a> projects, I&rsquo;ll often put together a collection&mdash;whether that&rsquo;s a short email of links or a 100-page, beautifully-designed <abbr title="Portable Document Format">PDF</abbr>&mdash;of past work that&rsquo;s contextualized to the particular problem they&rsquo;re asking us to help them solve.</p>

            <p>A good portfolio is no different. In fact, it may even be easier to put together than a services inquiry. With a services inquiry, I sometimes know very little about the company, and all I have to rely on for my response is a short conversation I may have had with a client. With a portfolio for an agency, you have all of their past work to look at to determine what&rsquo;s important to them. With a portfolio for a startup or product shop, you can look at the product(s) they make to understand what kind of company they are. For both, you can also research some of the people who work there to find out what qualities and skills they posses that landed them their jobs.</p>

            <p>Thinking of this as a proposal instead of a portfolio can justify why it should be custom every time and help free you from the standard thinking that they way to get a job is to send a link to your online junk drawer or past projects.</p>

