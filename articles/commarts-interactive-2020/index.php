                <p><span class="dm-dp-openingLines">When I was first learning design,</span> the <a href="https://www.commarts.com/">Communication Arts</a> annuals were often my go-to resource for best-in-class inspiration. I&rsquo;m thrilled to be on the jury for the <a href="https://www.commarts.com/competition/2020-interactive">2020 Interactive Awards</a> along with <a href="https://www.linkedin.com/in/gb2019/">Guillaume Braun</a> of <a href="https://www.akufen.ca/en">Akufen</a>, <a href="https://twitter.com/jacinte">Jacinte Faria</a> of <a href="https://www.stinkstudios.com/">Stink Studios</a>, <a href="https://twitter.com/hayhughes">Hayley Hughes</a> of <a href="https://www.shopify.com/">Shopify</a>, and <a href="https://www.linkedin.com/in/philliptiongson/">Phillip Tiongson</a> of <a href="https://www.potiondesign.com/">Potion</a>.</p>

                <div class="dm-c-carbonWrap">
                    <script async type="text/javascript" src="//cdn.carbonads.com/carbon.js?zoneid=1696&amp;serve=CVYD42T&amp;placement=danielmallcom" id="_carbonads_js"></script>
                </div><!-- .dm-c-carbonWrap -->

                <p>Historically, the work awarded in this show and others like it has typically been apps, microsites, installations, and other short-lived pieces, tied to some advertising campaign or marketing push. This makes sense, as awards like this tend to be highly coveted by ad agencies and production shops as a way to somewhat objectively endorse work in an otherwise unregulated industry. In <a href="https://www.commarts.com/magazine/2019-interactive">last year&rsquo;s Interactive Annual</a>, 31 of the 37 awarded pieces were tied to a campaign of some sort.</p>

                <p>That&rsquo;s not a complaint. I&rsquo;ve always loved work like Tool of North America&rsquo;s <a href="http://toolofna.com/work/amazon-jurassic-box">Jurassic Box Experience</a>, and I hope we continue to see that kind of submission.</p>

                <p>But I <em>do</em> hope to see some more work that typically goes unheralded. I&rsquo;d like to see more sites, not microsites. I&rsquo;d like to see apps that people use regularly, not apps that have a 6-week shelf life, where all that&rsquo;s left to reference it is a sizzle reel.</p>

                <p>I&rsquo;m optimistic that this isn&rsquo;t outside of the spirit of this competition. While the Commarts Interactive competition has traditionally only featured jurors that work in advertising, recent years have seen more jurors from product companies like <a href="https://twitter.com/megangmeeker">Megan Meeker</a> of <a href="https://www.lyft.com/">Lyft</a> and <a href="https://twitter.com/lbawcombe/">Libby Bawcombe</a> of <a href="https://www.npr.org/">NPR</a>. That&rsquo;s a noteworthy signal that product work can be as inspiring and exciting as advertising.</p>

                <p>I love that last year&rsquo;s jury awarded sites like <a href="https://cowboy.com/">Cowboy</a> by <a ref="https://ueno.co/">Ueno</a>, <a href="https://thecoolclub.co/">The Cool Club</a > by <a href="https://wonderlandindustry.com/">Wonderland</a>, and <a href="https://www.rapha.cc/us/en_US/">Rapha</a> by <a href="https://work.co/">Work &amp; Co.</a> These are sites that command the same level of polish found in any good ad campaign but perform a <em>business</em> function for the company in addition to a marketing function. This is the kind of work I think we should celebrate more.</p>

                <p>I&rsquo;d love to award work that demonstrates creative use of the highest level of color contrast ratios and works well on assistive devices. I&rsquo;d love to award work that&rsquo;s still useful when JavaScript fails. I&rsquo;d love to award work that shows smart thinking and strategy in addition to flawless execution and art direction. I&rsquo;d love to award work that serves business and user needs. I&rsquo;d love to award the <a ref="https://apps.apple.com/app/apple-store/id1358823008?mt=8">Flighty</a> app. I&rsquo;d love to award Gov.uk&rsquo;s <a href="https://www.gov.uk/get-ready-brexit-check">Get ready for Brexit</a> guide. I&rsquo;d love to award the <a href="https://apps.apple.com/us/app/lyft/id529379082">flagship Lyft app</a> in addition to the <a href="https://www.deeplocal.com/lyft-auto-tunes">Lyft Auto Tunes</a> campaign. I&rsquo;d love to award <a href="https://design.theguardian.com/">The Guardian&rsquo;s guide to digital design</a>. I&rsquo;d love to award the <a href="https://airtable.com/">Airtable</a h> and <a href="https://www.notion.so/">Notion</a> <em>platforms</em>, not solely their apps or sites.</p>

                <p>But I can only award work that&rsquo;s submitted.</p>
                
                <p>So, <a href="https://www.commarts.com/competition/2020-interactive">please submit that kind of work</a>! If you do, you have my word that, as a jury member, I&rsquo;ll do my darnedest to lobby for why brilliant, often utilitarian, people-serving work deserves a spot on our award shelves.</p>

